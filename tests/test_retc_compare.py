    assert np.isclose(p_retc["theta_r"], genf.theta_r, atol=atol)
    assert np.isclose(p_retc["theta_s"], genf.theta_s, atol=atol)
    assert np.isclose(p_retc["alpha"], genf.alpha, atol=atol)
    assert np.isclose(p_retc["n"], genf.n, atol=atol)
    assert np.isclose(p_retc["k_s"], genf.k_s, atol=atol)
